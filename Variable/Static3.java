package Java.Variable;

public class Static3 {
    static  int a;
    static float b;
    static  char c;
    static String s;
    static long l;
    static byte b1;
    static double d1;

    static void main() {
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(s);
        System.out.println(l);
        System.out.println(b1);
        System.out.println(d1);
    }
}
